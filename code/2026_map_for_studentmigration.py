def parse_domain_to_dict(text):
    """Parses 'Category\nValue%' strings into a dictionary."""
    if pd.isna(text) or not isinstance(text, str):
        return {}
    # Pattern: Matches non-percent characters followed by a newline and a digit
    matches = re.findall(r'([^%\n]+)\n(\d+)%', text)
    result = defaultdict(int)
    for name, val in matches:
        result[name.strip()] += int(val) # Sums values if a category appears twice
    return dict(result)

