def get_trace_data(origin_name):
    try:
        if origin_name == 'TOTAL':
            all_vals = cleaned_dfs['All'].sum(0)
            m_vals   = cleaned_dfs['Male'].sum(0)
            f_vals   = cleaned_dfs['Female'].sum(0)
        else:
            all_vals = cleaned_dfs['All'].loc[origin_name]
            m_vals   = cleaned_dfs['Male'].loc[origin_name]
            f_vals   = cleaned_dfs['Female'].loc[origin_name]

        res = pd.DataFrame({
            'Dest':   np.array(all_vals.index).flatten(),
            'Total':  np.array(all_vals.values).flatten(),
            'Male':   np.array(m_vals.values).flatten(),
            'Female': np.array(f_vals.values).flatten()
        })

        res['Dest'] = res['Dest'].astype(str).map(lambda x: country_map.get(x, x))
        res['ISO3'] = res['Dest'].map(to_iso3)
        res['MapLoc'] = np.where(res['ISO3'].notna(), res['ISO3'], res['Dest'])
        res = res[res['Total'] > 0].copy()
        if res.empty:
            return pd.DataFrame()
        res['LogTotal'] = np.log10(res['Total'].astype(float).clip(lower=1))
        return res
    except Exception as e:
        print("get_trace_data error:", e)
        return pd.DataFrame()

