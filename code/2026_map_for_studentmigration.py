def build_top15_distribution(res_map, origin_name):
    """
    res_map: output of get_trace_data (ALL countries)
    Returns: res_top with Total_Pct + A*_Seg + Male/Female_Seg for bars
    """
    if res_map is None or res_map.empty:
        return pd.DataFrame()

    # TOP 15 destinations by Total
    res_top = res_map.sort_values('Total', ascending=True).tail(15).copy()

    grand_total = res_top['Total'].sum()
    res_top['Total_Pct'] = (res_top['Total'] / grand_total * 100).round(1)

    # Age: real if available, else simulate
    if has_age_data and all(k in cleaned_dfs for k in ['A1', 'A2', 'A3', 'A4']):
        if origin_name == 'TOTAL':
            age_vals = {k: cleaned_dfs[k].sum(0) for k in ['A1','A2','A3','A4']}
        else:
            # if missing origin in age tables, fall back to zeros
            age_vals = {}
            for k in ['A1','A2','A3','A4']:
                if origin_name in cleaned_dfs[k].index:
                    age_vals[k] = cleaned_dfs[k].loc[origin_name]
                else:
                    age_vals[k] = pd.Series(0, index=cleaned_dfs['All'].columns)

        for k in ['A1','A2','A3','A4']:
            mapper = dict(zip(age_vals[k].index, age_vals[k].values))
            res_top[k] = res_top['Dest'].map(mapper).fillna(0)
    else:
        # simulate
        res_top['A1'] = res_top['Total'] * 0.15
        res_top['A2'] = res_top['Total'] * 0.35
        res_top['A3'] = res_top['Total'] * 0.30
        res_top['A4'] = res_top['Total'] * 0.20

    # Segment sizes for diverging bars
    for k in ['A1', 'A2', 'A3', 'A4']:
        res_top[f'{k}_Seg'] = (res_top[k] / res_top['Total'] * res_top['Total_Pct']).replace([np.inf, -np.inf], 0).fillna(0)

    for k in ['Male', 'Female']:
        res_top[f'{k}_Seg'] = (res_top[k] / res_top['Total'] * res_top['Total_Pct']).replace([np.inf, -np.inf], 0).fillna(0)

    return res_top

