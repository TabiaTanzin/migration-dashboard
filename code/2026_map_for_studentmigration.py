def make_discrete_scale(base_scale):
    stepped = []
    for i in range(len(base_scale) - 1):
        t0, c0 = base_scale[i]
        t1, _  = base_scale[i + 1]
        stepped.append([t0, c0])
        stepped.append([t1, c0])
    stepped.append([base_scale[-1][0], base_scale[-1][1]])
    return stepped

